-- 9-8. 建立管理員調整商品上下架狀態的安全 RPC 函數
CREATE OR REPLACE FUNCTION public.update_product_active_status(p_item_id text, p_is_active boolean, p_operator text)
RETURNS void AS $$
BEGIN
    -- 設定交易 Session 變數，以便讓觸發器知道是哪位管理員改的
    PERFORM set_config('my.app.operator', p_operator, true);
    IF p_is_active = true THEN
        PERFORM set_config('my.app.reference_id', 'PRODUCT_LIST', true);
    ELSE
        PERFORM set_config('my.app.reference_id', 'PRODUCT_DELIST', true);
    END IF;
    
    UPDATE public.items_list
    SET is_active = p_is_active
    WHERE item_id = p_item_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

