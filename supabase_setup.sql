-- 9-3. 建立變更監聽觸發器函數 (log_inventory_changes)
CREATE OR REPLACE FUNCTION public.log_inventory_changes()
RETURNS TRIGGER AS $$
DECLARE
    v_change_qty integer;
    v_change_type text := 'MANUAL_ADJUST';
    v_ref_id text := NULL;
    v_operator text := 'system';
BEGIN
    -- 試圖從 session 變數中讀取操作人員及參考單號 (設定方式如: SET LOCAL my.app.operator = 'admin')
    BEGIN
        v_operator := current_setting('my.app.operator', true);
        IF v_operator IS NULL OR v_operator = '' THEN
            v_operator := 'system';
        END IF;
    EXCEPTION WHEN OTHERS THEN
        v_operator := 'system';
    END;

    BEGIN
        v_ref_id := current_setting('my.app.reference_id', true);
    EXCEPTION WHEN OTHERS THEN
        v_ref_id := NULL;
    END;

    -- 1. 檢查上下架狀態變更
    IF OLD.is_active IS DISTINCT FROM NEW.is_active THEN
        IF NEW.is_active = true THEN
            v_change_type := 'PRODUCT_LIST';
        ELSE
            v_change_type := 'PRODUCT_DELIST';
        END IF;
        
        INSERT INTO public.inventory_logs (
            item_id, item_name, change_type, change_qty, old_qty, new_qty, reference_id, operator
        ) VALUES (
            NEW.item_id, NEW.item_name, v_change_type, 0, OLD.inventory, NEW.inventory, v_ref_id, v_operator
        );
    END IF;

    -- 2. 檢查庫存數量變更
    v_change_qty := NEW.inventory - OLD.inventory;
    IF v_change_qty <> 0 THEN
        IF v_operator = 'system' THEN
            IF v_change_qty < 0 THEN
                v_change_type := 'ORDER_SUBMIT';
            ELSE
                v_change_type := 'ORDER_DELETE';
            END IF;
        ELSE
            -- 若為管理員操作，且非系統預設，可由 reference_id 判別，否則為 MANUAL_ADJUST
            IF v_ref_id IS NOT NULL AND v_ref_id <> '' AND v_ref_id <> 'MANUAL_ADJUST' THEN
                v_change_type := v_ref_id;
            ELSE
                v_change_type := 'MANUAL_ADJUST';
            END IF;
        END IF;

        INSERT INTO public.inventory_logs (
            item_id, item_name, change_type, change_qty, old_qty, new_qty, reference_id, operator
        ) VALUES (
            NEW.item_id, NEW.item_name, v_change_type, v_change_qty, OLD.inventory, NEW.inventory, v_ref_id, v_operator
        );
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 9-4. 建立觸發器
DROP TRIGGER IF EXISTS tr_log_inventory_changes ON public.items_list;
CREATE TRIGGER tr_log_inventory_changes
AFTER UPDATE OF inventory, is_active ON public.items_list
FOR EACH ROW
EXECUTE FUNCTION public.log_inventory_changes();

