-- 7-4. 建立安全登入比對函數 (verify_admin_login)
-- 回傳代碼: 0 = 帳密錯誤, 1 = 登入成功(已啟用), 2 = 審核中(未啟用)
CREATE OR REPLACE FUNCTION public.verify_admin_login(p_username text, p_password text)
RETURNS integer AS $$
DECLARE
    v_hash text;
    v_active boolean;
BEGIN
    SELECT password_hash, is_active INTO v_hash, v_active 
    FROM public.admin_accounts 
    WHERE username = p_username;
    
    -- 帳密錯誤
    IF v_hash IS NULL OR v_hash <> crypt(p_password, v_hash) THEN
        RETURN 0; 
    END IF;
    
    -- 已啟用
    IF v_active THEN
        RETURN 1;
    -- 審核中
    ELSE
        RETURN 2;
    END IF;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 7-5. 建立管理員安全註冊函數 (register_admin_account)
-- 回傳代碼: 0 = 帳號已存在, 1 = 註冊成功待審核
CREATE OR REPLACE FUNCTION public.register_admin_account(p_username text, p_password text, p_full_name text, p_staff_id text, p_email text)
RETURNS integer AS $$
BEGIN
    IF EXISTS (SELECT 1 FROM public.admin_accounts WHERE username = p_username) THEN
        RETURN 0;
    END IF;

    INSERT INTO public.admin_accounts (username, password_hash, full_name, staff_id, email, is_active)
    VALUES (p_username, crypt(p_password, gen_salt('bf')), p_full_name, p_staff_id, p_email, false);
    
    RETURN 1;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 7-6. 建立帳號後台管理函數 (超級管理員專用 API)
CREATE OR REPLACE FUNCTION public.get_admin_accounts()
RETURNS TABLE (username text, full_name text, staff_id text, email text, is_active boolean, created_at timestamp with time zone) AS $$
BEGIN
