-- 9-6. 建立管理員手動調整庫存的安全 RPC 函數
CREATE OR REPLACE FUNCTION public.update_inventory_manual(p_item_id text, p_new_qty integer, p_operator text)
RETURNS void AS $$
BEGIN
    -- 設定交易 Session 變數，以便讓觸發器知道是哪位管理員改的
    PERFORM set_config('my.app.operator', p_operator, true);
    PERFORM set_config('my.app.reference_id', 'MANUAL_ADJUST', true);
    
    UPDATE public.items_list
    SET inventory = p_new_qty
    WHERE item_id = p_item_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

