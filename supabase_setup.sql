-- 4. 建立庫存扣減 RPC 函數 (處理後台手動修改等防呆)
CREATE OR REPLACE FUNCTION public.decrease_inventory(item_ids text[], quantities integer[])
RETURNS void AS $$
DECLARE
    i integer;
BEGIN
    FOR i IN 1..array_length(item_ids, 1) LOOP
        -- 扣減庫存
        UPDATE public.items_list
        SET inventory = inventory - quantities[i]
        WHERE item_id = item_ids[i];
        
        -- 檢查是否庫存不足扣至負數，如果是則拋出異常阻斷交易
        IF EXISTS (SELECT 1 FROM public.items_list WHERE item_id = item_ids[i] AND inventory < 0) THEN
            RAISE EXCEPTION '商品 ID % 庫存不足，無法扣減！', item_ids[i];
        END IF;
    END LOOP;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 4-2. 建立原子性訂單提交 RPC 函數 (前台專用，主檔、明細與庫存扣減一鍵完成，確保交易完整性)
CREATE OR REPLACE FUNCTION public.create_promo_application(
    p_task_id text,
    p_applicant_name text,
    p_staff_id text,
    p_phone text,
    p_email text,
    p_department text,
    p_cost_center text,
    p_reason text,
    p_total_amount numeric,
    p_item_ids text[],
    p_item_names text[],
    p_prices numeric[],
    p_quantities integer[]
)
RETURNS void AS $$
DECLARE
    i integer;
BEGIN
    -- 設定 session 變數以供庫存日誌觸發器讀取
    PERFORM set_config('my.app.operator', 'system', true);
    PERFORM set_config('my.app.reference_id', p_task_id, true);

    -- 1. 新增申請主檔
    INSERT INTO public.applications (
        task_id, applicant_name, staff_id, status, applicant_phone, 
        applicant_email, department, cost_center, reason, total_amount
    ) VALUES (
        p_task_id, p_applicant_name, p_staff_id, 'SUBMITTED', p_phone, 
        p_email, p_department, p_cost_center, p_reason, p_total_amount
    );

    -- 2. 迴圈寫入明細並扣減庫存
    FOR i IN 1..array_length(p_item_ids, 1) LOOP
        -- 寫入明細
        INSERT INTO public.application_items (
            task_id, item_id, item_name, unit_price, quantity, subtotal
        ) VALUES (
            p_task_id, p_item_ids[i], p_item_names[i], p_prices[i], p_quantities[i], p_prices[i] * p_quantities[i]
        );

        -- 扣減庫存
        UPDATE public.items_list
        SET inventory = inventory - p_quantities[i]
        WHERE item_id = p_item_ids[i];
        
        -- 檢查庫存是否不足
        IF EXISTS (SELECT 1 FROM public.items_list WHERE item_id = p_item_ids[i] AND inventory < 0) THEN
            RAISE EXCEPTION '商品「%」目前庫存不足！', p_item_names[i];
        END IF;
    END LOOP;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 4-3. 建立刪除申請明細時自動回補庫存的觸發器 (Trigger)
CREATE OR REPLACE FUNCTION public.restore_inventory_on_delete()
RETURNS TRIGGER AS $$
BEGIN
    UPDATE public.items_list
    SET inventory = inventory + OLD.quantity
    WHERE item_id = OLD.item_id;
    RETURN OLD;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS tr_restore_inventory_on_delete ON public.application_items;
CREATE TRIGGER tr_restore_inventory_on_delete
AFTER DELETE ON public.application_items
FOR EACH ROW
EXECUTE FUNCTION public.restore_inventory_on_delete();


